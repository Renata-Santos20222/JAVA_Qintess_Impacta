package Capitulo_06;

public class MetodosEstaticosEMetodosNaoEstaticos {
	
	// Declarando um metodo com modificador estatico
	public static void metodoEstatico(){
		System.out.println("Este é o conteúdo do método estático.\n");
	}
	
	// Declarando um método sem o modificador static
	public void metodoNaoEstatico() {
		System.out.println("Este é o conteúdo do método não estático.\n");
	}

}