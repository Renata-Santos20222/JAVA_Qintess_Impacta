package Atividades_Extras.Imprimir20VezesComFor;

public class Imprimir20VezesComFor {

	public static void main(String[] args) {

		// Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar Algoritmos!”. Utilize o comando de repetição FOR.
		for (int i = 0; i < 20; i++) {
			System.out.println((i+1) + " - Eu gosto de estudar Algoritmos!");
		}

	}

}
