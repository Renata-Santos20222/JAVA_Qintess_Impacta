package Atividades_Extras.Imprimir20VezesComDoWhile;

public class Imprimir20VezesComDoWhile {

	public static void main(String[] args) {

		// Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar Algoritmos!”. Utilize o comando de repetição Enquanto (Do While).
		int i = 0;

		do {
			System.out.println((i+1) + " - Eu gosto de estudar Algoritmos!");
			i++;
		}while (i < 20);

	}

}
