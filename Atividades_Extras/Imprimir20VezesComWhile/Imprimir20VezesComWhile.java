package Atividades_Extras.Imprimir20VezesComWhile;

public class Imprimir20VezesComWhile {

	public static void main(String[] args) {

		// Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar Algoritmos!”. Utilize o comando de repetição Enquanto (While).
		int i = 0;
		while (i < 20) {
			System.out.println((i+1) + " - Eu gosto de estudar Algoritmos!");
			i++;
		}

	}

}
