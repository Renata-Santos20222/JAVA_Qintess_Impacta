package Capitulo_02;

public class Variaveis {

	public static void main(String[] args) {

		// Declarar variáveis simples com definição e tipo
		int a = 56, b;
		int idade = 18;
		char genero = 'f';
		String curso = "Linguagem java", escola = "Impacta";
		double VAL_PI = 3.1415;
		float faixa = 4.21f;
		boolean resposta = false;
		
		// Atribuindo valor a variável b
		b = 20;
		
		// Exibindo os valores das variáveis.
		System.out.println("a = " + a + "\n" + "b = " + b);
		System.out.println("idade = " + idade);
		System.out.println("genero = " + genero);
		System.out.println("O curso é : " + curso + "\n" + "E a escola é: " + escola);
		System.out.println("VAL_PI = " + VAL_PI);
		System.out.println("faixa = " + faixa);
		System.out.println("resposta = " + resposta + "\n");

	}

}
